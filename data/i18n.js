/**
 * Translate a key to the current language.
 * Supports template substitution: t('toast.saved', { name: 'My Game' })
 * Fallback chain: requested lang → Norwegian → raw key
 */
window.t = function(key, vars) {
  const lang = (window.state && window.state.lang) ? window.state.lang : 'no';
  let str = UI_TEXT[lang]?.[key] ?? UI_TEXT['no']?.[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replaceAll('{' + k + '}', String(v));
    }
  }
  return str;
};
